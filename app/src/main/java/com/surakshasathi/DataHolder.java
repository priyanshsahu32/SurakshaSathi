package com.surakshasathi;

public class DataHolder {
    private String text1;
    private String text2;

    public DataHolder(String text1, String text2) {
        this.text1 = text1;
        this.text2 = text2;
    }

    public String getText1() {
        return text1;
    }

    public String getText2() {
        return text2;
    }
}

